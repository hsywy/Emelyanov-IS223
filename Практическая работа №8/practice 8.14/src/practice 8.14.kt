fun sum(a: Int, b: Int): Int {
    return a + b
}

fun main() {
    val number1 = 5
    val number2 = 10

    // Вызов функции sum для вычисления суммы
    val result = sum(number1, number2)

    // Вывод результата на экран
    println("Сумма $number1 и $number2 равна $result")
}